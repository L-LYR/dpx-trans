#!/bin/bash

buildtype=${1:-"debug"}

printf "\n////////////////////////////////////////////////////////////////////////////////\n"
printf "                           default buildtype: %s\n" "$buildtype"
printf "////////////////////////////////////////////////////////////////////////////////\n\n"
CC=clang-18 CXX=clang++-18 meson setup --buildtype="$buildtype" build
printf "\n////////////////////////////////////////////////////////////////////////////////\n"
printf "                      use 'meson compile -C build' to build\n"
printf "////////////////////////////////////////////////////////////////////////////////\n\n"
